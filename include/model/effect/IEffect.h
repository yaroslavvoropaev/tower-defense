#pragma once

#include <memory>

namespace TowerDefense {
    class IEnemy;

    /**
     * @brief Интерфейс, представляющий эффект, который может быть применен к врагу.
     *
     * IEffect определяет поведение временных эффектов, таких как
     * замедление, яд, ослабление и т.д., которые могут влиять на врагов
     * в игре. Поддерживает паттерн "Прототип" через метод clone() для создания копий.
     */
    class IEffect {
    public:
        /**
         * @brief Применяет эффект к указанному врагу.
         *
         * Выполняет начальное применение эффекта, например, уменьшение скорости,
         * нанесение начального урона или установка состояния.
         *
         * @param enemy Ссылка на врага, к которому применяется эффект.
         */
        virtual void apply_effect(IEnemy &enemy) = 0;

        /**
         * @brief Создает глубокую копию эффекта.
         *
         * Реализует паттерн "Прототип", позволяя создавать копии эффектов
         * для применения к нескольким врагам или для сериализации.
         *
         * @return std::unique_ptr<IEffect> Умный указатель на копию эффекта.
         */
        virtual std::unique_ptr<IEffect> clone() = 0;

        /**
         * @brief Обновляет состояние эффекта с течением времени.
         *
         * Вызывается каждый игровой тик для обработки продолжительных эффектов:
         * нанесение периодического урона, обновление длительности, проверка
         * условий завершения.
         *
         * @param enemy Ссылка на врага, на которого действует эффект.
         * @param delta_time Время, прошедшее с предыдущего обновления (в секундах).
         */
        virtual void update_effect(IEnemy &enemy, float delta_time) = 0;

        /**
         * @brief Проверяет, завершился ли эффект.
         *
         * Эффект может завершиться по истечении времени, по достижению
         * определенного количества тиков или при выполнении условий.
         *
         * @return true, если эффект завершился и должен быть удален.
         * @return false, если эффект все еще активен.
         */
        virtual bool is_finished() = 0;

        /**
         * @brief Виртуальный деструктор для корректного наследования.
         */
        virtual ~IEffect() = default;
    };
}