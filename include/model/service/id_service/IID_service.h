#pragma once

#include "id.h"

namespace TowerDefense {
    /**
     * @brief Интерфейс сервиса для генерации и управления уникальными идентификаторами.
     *
     * IID_service предоставляет методы для получения новых уникальных ID
     * и управления текущим состоянием счетчика идентификаторов. Используется
     * для присвоения уникальных идентификаторов игровым объектам.
     */
    class IID_service {
    public:
        /**
         * @brief Генерирует и возвращает новый уникальный идентификатор.
         *
         * @return ID Новый уникальный идентификатор.
         */
        [[nodiscard]] virtual ID get_id() = 0;

        /**
         * @brief Устанавливает начальное значение для генератора идентификаторов.
         *
         * Используется при загрузке состояния игры для восстановления
         * последовательности идентификаторов.
         *
         * @param id Начальное значение для установки.
         */
        virtual void set_id(ID id) = 0;

        /**
         * @brief Виртуальный деструктор для обеспечения корректного наследования.
         */
        virtual ~IID_service() = default;
    };
}