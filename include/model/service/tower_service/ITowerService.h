#pragma once

#include "IAttackVisitor.h"
#include <memory>
#include <typeindex>

namespace TowerDefense {
    /**
     * @brief Интерфейс сервиса для управления атаками башен.
     *
     * ITowerService отвечает за обработку атак всех башен на игровом поле.
     * Использует паттерн "Посетитель" для реализации различных типов атак
     * для разных типов башен. Поддерживает как последовательные, так и
     * параллельные вычисления для оптимизации производительности.
     */
    class ITowerService {
    public:
        /**
         * @brief Выполняет атаку всех башен последовательно.
         *
         * @param dt Время, прошедшее с предыдущего обновления (в секундах).
         * @return std::vector<AttackInfo> Информация о выполненных атаках.
         */
        virtual std::vector<AttackInfo> attack_all(float dt) = 0;

        /**
         * @brief Выполняет атаку всех башен параллельно.
         *
         * Использует многопоточность для оптимизации производительности.
         *
         * @param dt Время, прошедшее с предыдущего обновления (в секундах).
         * @return std::vector<AttackInfo> Информация о выполненных атаках.
         */
        virtual std::vector<AttackInfo> attack_all_par(float dt) = 0;

        /**
         * @brief Регистрирует посетителя атак для определенного типа башни.
         *
         * Каждый тип башни может иметь своего
         * посетителя, который определяет логику атаки, выбор целей и нанесение урона.
         *
         * @param type_index Индекс типа башни, для которого регистрируется посетитель.
         * @param visitor Умный указатель на объект посетителя атак.
         *                Владение объектом передается сервису.
         */
        virtual void add_visitor(std::type_index type_index, std::unique_ptr<IAttackVisitor> visitor) = 0;

        /**
         * @brief Виртуальный деструктор для обеспечения корректного наследования.
         */
        virtual ~ITowerService() = default;
    };
}