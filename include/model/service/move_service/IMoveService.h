#pragma once

namespace TowerDefense {
    /**
     * @brief Интерфейс сервиса для управления перемещением объектов.
     *
     * IMoveService отвечает за обновление позиций всех подвижных объектов
     * на игровом поле. Поддерживает как последовательное,
     * так и параллельное выполнение для оптимизации производительности.
     */
    class IMoveService {
    public:
        /**
         * @brief Выполняет параллельное перемещение всех объектов.
         *
         * Использует многопоточность или другие методы параллельной обработки
         * для оптимизации производительности на многоядерных системах.
         *
         * @param dt Время, прошедшее с предыдущего обновления (в секундах).
         */
        virtual void move_par(float dt) = 0;

        /**
         * @brief Выполняет последовательное перемещение всех объектов.
         *
         * Альтернатива параллельной версии, может использоваться для отладки
         * или на системах с ограниченной поддержкой многопоточности.
         *
         * @param dt Время, прошедшее с предыдущего обновления (в секундах).
         */
        virtual void move(float dt) = 0;

        /**
         * @brief Виртуальный деструктор для обеспечения корректного наследования.
         */
        virtual ~IMoveService() = default;
    };
}