#pragma once

#include <memory>
#include <typeindex>

#include "id.h"
#include "IUpgradeVisitor.h"

namespace TowerDefense {
    /**
     * @brief Интерфейс сервиса для управления экономикой и улучшениями.
     *
     * IEconomyService отвечает за обработку операций улучшений игровых объектов
     * с использованием паттерна "Посетитель". Сервис
     * регистрирует различных посетителей для разных типов объектов и управляет
     * процессом улучшений, включая проверку возможности апгрейда и его выполнение.
     */
    class IEconomyService {
    public:
        /**
         * @brief Регистрирует посетителя улучшений для определенного типа объекта.
         *
         * Каждый тип объекта (например, конкретный тип башни) может иметь свой
         * посетитель, который определяет логику улучшений и стоимость апгрейда.
         *
         * @param type_index Индекс типа объекта, для которого регистрируется посетитель.
         * @param visitor Умный указатель на объект посетителя улучшений.
         *                Владение объектом передается сервису.
         */
        virtual void add_visitor(std::type_index type_index, std::unique_ptr<IUpgradeVisitor> visitor) = 0;

        /**
         * @brief Выполняет улучшение объекта по его идентификатору.
         *
         * Метод находит объект по ID, определяет его тип, находит соответствующего
         * посетителя и выполняет улучшение, если это возможно.
         *
         * @param id Идентификатор объекта для улучшения.
         * @return bool true, если улучшение успешно выполнено; false, если улучшение
         *         невозможно или произошла ошибка.
         */
        virtual bool upgrade(ID id) = 0;

        /**
         * @brief Виртуальный деструктор для обеспечения корректного наследования.
         */
        virtual ~IEconomyService() = default;
    };
}