#pragma once

namespace TowerDefense {
    /**
     * @brief Интерфейс сервиса для управления атаками врагов.
     *
     * IEnemyService отвечает за обработку атак всех врагов на игровом поле.
     * Координирует процесс нанесения урона защищаемым объектам
     * всеми активными врагами.
     */
    class IEnemyService {
    public:
        /**
         * @brief Выполняет атаку всех врагов на игровом поле.
         *
         * Метод обрабатывает логику атаки для каждого врага, включая проверку
         * дистанции до целей, расчет урона и применение урона к защищаемым объектам.
         */
        virtual void attack_all() = 0;

        /**
         * @brief Виртуальный деструктор для обеспечения корректного наследования.
         */
        virtual ~IEnemyService() = default;
    };
}