#pragma once

#include "dto.h"

namespace TowerDefense {
    class ITower;

    /**
     * @brief Интерфейс посетителя для обработки улучшений башен.
     *
     * IUpgradeVisitor реализует паттерн "Посетитель" для обработки логики улучшения
     * различных типов башен. Каждый конкретный посетитель определяет специфическую
     * логику улучшения для определенного типа башни, включая изменение характеристик
     * и стоимости апгрейда.
     */
    class IUpgradeVisitor {
    public:
        virtual ~IUpgradeVisitor() = default;

        /**
         * @brief Обрабатывает улучшение башни.
         *
         * Этот метод вызывается для выполнения улучшения конкретной башни.
         * Включает в себя проверку возможности улучшения, изменение характеристик
         * башни (урон, диапазон, скорострельность) и обновление информации об уровне.
         *
         * @param tower Ссылка на башню, которую нужно улучшить.
         * @param level_dto Ссылка на DTO с информацией об уровне улучшения.
         *                  Содержит данные о новых характеристиках и стоимости.
         */
        virtual void visit(ITower &tower, LevelDTO &level_dto) = 0;
    };
}