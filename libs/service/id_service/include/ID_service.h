#pragma once

#include "id.h"
#include "IID_service.h"

namespace TowerDefense {
    /**
     * @brief Конкретная реализация сервиса для генерации уникальных идентификаторов.
     *
     * ID_service предоставляет простой счетчик для генерации последовательных
     * уникальных ID. Поддерживает установку начального значения для восстановления
     * состояния.
     */
    class ID_service final : public IID_service {
        ID counter_; ///< Текущее значение счетчика ID

    public:
        /**
         * @brief Конструктор сервиса ID.
         *
         * @param counter Начальное значение счетчика (по умолчанию 0).
         */
        explicit ID_service(ID counter = 0) : counter_(counter) {}

        /**
         * @brief Генерирует и возвращает новый уникальный идентификатор.
         *
         * Увеличивает внутренний счетчик и возвращает предыдущее значение.
         *
         * @return ID Новый уникальный идентификатор.
         */
        [[nodiscard]] ID get_id() override;

        /**
         * @brief Устанавливает начальное значение для генератора идентификаторов.
         *
         * Используется при загрузке состояния игры для восстановления
         * последовательности идентификаторов.
         *
         * @param id Начальное значение для установки.
         */
        void set_id(ID id) override;

        /**
         * @brief Деструктор сервиса ID.
         */
        ~ID_service() override = default;
    };
}